#include "delay.h"

/*************************************************************************
函 数 名：delay_ms
函数功能：不精确延时t ms
*************************************************************************/
void delay_ms(u32 t)
{
	u32 i = 0;
	for( i = 0; i < t; i++ )
	{
		u32 a = 42000;
		while(a--);
	}
}

/*************************************************************************
函 数 名：delay_us
函数功能：不精确延时t us
*************************************************************************/
void delay_us(u32 t)
{
	u32 i=0;
	for( i = 0; i < t; i++ )
	{
		u32 a = 40;
		while(a--);
	}
}




